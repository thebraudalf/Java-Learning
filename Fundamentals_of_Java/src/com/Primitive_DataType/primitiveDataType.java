package com.Primitive_DataType;

public class primitiveDataType {
    public static void main(String[] args) {
        // int data type stores integers
        int num = 232322323;

        // char data type stores letter
        char word = 't';

        // float data type stores float value or decimal value (you can write float with "F" or "f" letter)
        float floatValue = 32233.23f;

        // long int data type stores values more than int data type (you can write long int with "L" letter)
        long num2 = 232323232323L;

        // double data type stores values more than float data type
        double floatValue2 = 233322.322;

        // boolean data type stores true and false
        boolean booleanValue = false;

        // Wrapper classes which converts data types like int, char to object and more data types some are
        // Integer class is used to convert int data type to instance of a class or object
        // Character class is used to convert char data type to instance of a class or object

    }
}
