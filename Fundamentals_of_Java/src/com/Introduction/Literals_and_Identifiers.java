package com.Introduction;

public class Literals_and_Identifiers {
    public static void main(String[] args) {

        // All the reserved words like int, and float, etc. are Literals.
        // Identifiers are those which have to give name like function, variable, and class, etc.


        // if you want to write big numbers
        // write like this
        int num = 200_000_00;

        System.out.println(num); // 20000000

        // not this else error occurred
        // int num2 = 200,000,00;

        // System.out.println(num2); // java: <identifier> expected
    }
}

